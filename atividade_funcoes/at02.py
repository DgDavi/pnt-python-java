def quilometros_para_milhas(quilometros):
    return quilometros * 0.621371

def metros_para_centimetros(metros):
    return metros * 100

def litros_para_mililitros(litros):
    return litros * 1000

print(quilometros_para_milhas(5))  
print(metros_para_centimetros(3))  
print(litros_para_mililitros(2))   