def quilometros_para_milhas(quilometros):
    return quilometros * 0.621371

def metros_para_centimetros(metros):
    return metros * 100

def litros_para_mililitros(litros):
    return litros * 1000

# Exemplos de uso
print(quilometros_para_milhas(5))  # Exemplo: 5 km para milhas
print(metros_para_centimetros(3))  # Exemplo: 3 m para cm
print(litros_para_mililitros(2))   # Exemplo: 2 L para mL